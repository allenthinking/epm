package org.easyworld.pm.bean;

public class CountryPMBean {
	private String countryName;
	private String befMoney;
	private String urMoney;
	private String countryCode;
	private String countryValue;

	public String getCountryValue() {
		return countryValue;
	}

	public void setCountryValue(String countryValue) {
		this.countryValue = countryValue;
	}

	public String getCountryName() {
		return countryName;
	}

	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}

	public String getBefMoney() {
		return befMoney;
	}

	public void setBefMoney(String befMoney) {
		this.befMoney = befMoney;
	}

	public String getUrMoney() {
		return urMoney;
	}

	public void setUrMoney(String urMoney) {
		this.urMoney = urMoney;
	}

	public String getCountryCode() {
		return countryCode;
	}

	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}

}
