package org.easyworld.pm.bean;

public class PmProjectCommonBean {
	private String jspBasicPath;
	
	private String springMVCJSPPrefix;
	
	public String getSpringMVCJSPPrefix() {
		return springMVCJSPPrefix;
	}

	public void setSpringMVCJSPPrefix(String springMVCJSPPrefix) {
		this.springMVCJSPPrefix = springMVCJSPPrefix;
	}

	public String getJspBasicPath() {
		return jspBasicPath;
	}

	public void setJspBasicPath(String jspBasicPath) {
		this.jspBasicPath = jspBasicPath;
	}
	
	
}
